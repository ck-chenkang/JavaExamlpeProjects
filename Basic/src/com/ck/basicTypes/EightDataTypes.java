package com.ck.basicTypes;

public class EightDataTypes {

    public static void main(String[] args) {
        /**
         *  java共有8种基本类型，六种数字（4种整型，两种浮点），一种字符，一种布尔
         *  4种整型： byte，short，int，long
         *      默认值: 0, 0, 0，0L
         *      位数：8,16,32,64
         *      使用方法：
         *          byte a = 100;
         *          short a = 100;
         *          int a = 100;
         *          long a = 100L; 不用L会报错
         *          Long a = 100L;
         *  2种浮点：float，double
         *  字符： char
         *  布尔： boolean
         */
        // 获取位数：
        System.out.println(Byte.SIZE);

        // 使用方法：
        byte a1 = 100;
        short a2 = 100;
        int a3 = 100;
        long a4 = 100L;
        Long a5 = 100L;





    }
}
